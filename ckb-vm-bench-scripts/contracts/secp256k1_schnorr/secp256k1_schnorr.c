#include "secp256k1.h"
#include "secp256k1_recovery.h"

int main() {
    const unsigned char sig_raw[64] = {
        0x25, 0x62, 0x91, 0x0f, 0xad, 0xe5, 0x80, 0x34, 0xf3, 0xe0, 0xd1, 0xa1, 0x00, 0x90, 0x71, 0xe7,
        0xf3, 0xc3, 0x53, 0x17, 0x87, 0xa5, 0x19, 0xe2, 0x57, 0xd3, 0xa7, 0xfd, 0xfa, 0x5c, 0x19, 0xea,
        0x6c, 0xa4, 0x0c, 0xf4, 0xdc, 0xbd, 0x33, 0x2a, 0x1c, 0x79, 0x88, 0xb1, 0x3e, 0x07, 0x5c, 0xb1,
        0xf9, 0x4a, 0xc3, 0xd6, 0xfe, 0x5b, 0x4c, 0xc9, 0x65, 0x5a, 0x8d, 0x09, 0x6f, 0xf9, 0x3e, 0x6d,
    };
    const unsigned char msg[32] = {
        0x72, 0xa9, 0x63, 0xcd, 0xfb, 0x01, 0xbc, 0x37, 0xcd, 0x28, 0x31, 0x06, 0x87, 0x5f, 0xf1, 0xf0,
        0x7f, 0x02, 0xbc, 0x9a, 0xd6, 0x12, 0x1b, 0x75, 0xc3, 0xd1, 0x76, 0x29, 0xdf, 0x12, 0x8d, 0x4e,
    };
    int rec = 1;

    const secp256k1_context *ctx = secp256k1_context_create(SECP256K1_CONTEXT_VERIFY);
    secp256k1_ecdsa_recoverable_signature sig;
    if (!secp256k1_ecdsa_recoverable_signature_parse_compact(ctx, &sig, sig_raw, rec)) {
        return 1;
    }
    secp256k1_pubkey pubkey;
    if (!secp256k1_ecdsa_recover(ctx, &pubkey, &sig, msg)) {
        return 2;
    }
    unsigned char pubkey_data[33];
    size_t pubkey_size = 33;
    if (!secp256k1_ec_pubkey_serialize(ctx, pubkey_data, &pubkey_size, &pubkey, SECP256K1_EC_COMPRESSED)) {
        return 3;
    }
    unsigned char expect_data[33] = {
        0x02, 0x79, 0xbe, 0x66, 0x7e, 0xf9, 0xdc, 0xbb, 0xac, 0x55, 0xa0, 0x62, 0x95, 0xce, 0x87, 0x0b, 0x07,
        0x02, 0x9b, 0xfc, 0xdb, 0x2d, 0xce, 0x28, 0xd9, 0x59, 0xf2, 0x81, 0x5b, 0x16, 0xf8, 0x17, 0x98,
    };
    for (int i = 0; i < 33; i++) {
        if (pubkey_data[i] != expect_data[i]) {
            return 4;
        }
    }
    return 0;
}
