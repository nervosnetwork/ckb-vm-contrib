import hashlib


def test_ax1000000():
    h = hashlib.sha256()
    for _ in range(1000000):
        h.update(b'a')
    r = h.digest()
    assert bytearray(r) == bytearray([
        0xcd, 0xc7, 0x6e, 0x5c, 0x99, 0x14, 0xfb, 0x92, 0x81, 0xa1, 0xc7, 0xe2, 0x84, 0xd7, 0x3e, 0x67,
        0xf1, 0x80, 0x9a, 0x48, 0xa4, 0x97, 0x20, 0x0e, 0x04, 0x6d, 0x39, 0xcc, 0xc7, 0x11, 0x2c, 0xd0,
    ])


def test_chain():
    m = bytearray(32)
    for _ in range(25000):
        h = hashlib.sha256()
        h.update(m)
        m = bytearray(h.digest())
    assert bytearray(m) == bytearray([
        0xde, 0x49, 0x2c, 0x0e, 0x28, 0xd3, 0x77, 0x06, 0xda, 0xc0, 0x48, 0x97, 0xe9, 0xc8, 0x25, 0xee,
        0x01, 0x4b, 0x1e, 0x85, 0xbc, 0x27, 0x7b, 0x3c, 0xdd, 0xbd, 0x9a, 0x56, 0x64, 0x51, 0xd7, 0xb6,
    ])


