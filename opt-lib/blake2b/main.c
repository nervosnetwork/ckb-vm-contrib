int test_chain() {
    uint8_t hash[32] = {0};
    for (int i = 0; i < 1000; i++) {
        uint8_t tmp[32];
        blake2b(tmp, 32, hash, 32, NULL, 0);
        __builtin_memcpy(hash, tmp, 32);
    }
    uint8_t want[32] = {
        0x65, 0x11, 0x43, 0x6e, 0xd9, 0xa4, 0x38, 0x6a, 0xc9, 0xb2, 0xc5,
        0x2d, 0xaf, 0x9c, 0x64, 0x9f, 0xc6, 0x61, 0xb6, 0x78, 0xfb, 0x90,
        0xd3, 0x97, 0x7c, 0x9c, 0x18, 0x79, 0xc5, 0xa5, 0xd9, 0xeb,
    };
    return __builtin_memcmp(hash, want, 32);
}

int main() {
    if (test_empty() != 0) {
        return 1;
    }
    if (test_abc() != 0) {
        return 1;
    }
    if (test_hello_world() != 0) {
        return 1;
    }
    if (test_chain() != 0) {
        return 1;
    }
    return 0;
}
