import hashlib


def blake2b_256(data: bytes) -> bytearray:
    return bytearray(hashlib.blake2b(data, digest_size=32).digest())


def test_empty():
    r = blake2b_256(b'')
    assert r == bytearray([
        0x0e, 0x57, 0x51, 0xc0, 0x26, 0xe5, 0x43, 0xb2, 0xe8, 0xab, 0x2e, 0xb0, 0x60, 0x99, 0xda, 0xa1,
        0xd1, 0xe5, 0xdf, 0x47, 0x77, 0x8f, 0x77, 0x87, 0xfa, 0xab, 0x45, 0xcd, 0xf1, 0x2f, 0xe3, 0xa8,
    ])


def test_abc():
    r = blake2b_256(b'abc')
    assert r == bytearray([
        0xbd, 0xdd, 0x81, 0x3c, 0x63, 0x42, 0x39, 0x72, 0x31, 0x71, 0xef, 0x3f, 0xee, 0x98, 0x57, 0x9b,
        0x94, 0x96, 0x4e, 0x3b, 0xb1, 0xcb, 0x3e, 0x42, 0x72, 0x62, 0xc8, 0xc0, 0x68, 0xd5, 0x23, 0x19,
    ])


def test_hello_world():
    r = blake2b_256(b'Hello, World!')
    assert r == bytearray([
        0x51, 0x1b, 0xc8, 0x1d, 0xde, 0x11, 0x18, 0x08, 0x38, 0xc5, 0x62, 0xc8, 0x2b, 0xb3, 0x5f, 0x32,
        0x23, 0xf4, 0x60, 0x61, 0xeb, 0xde, 0x4a, 0x95, 0x5c, 0x27, 0xb3, 0xf4, 0x89, 0xcf, 0x1e, 0x03,
    ])


def test_chain():
    r = bytearray(32)
    for _ in range(1000):
        r = blake2b_256(r)
    assert r == bytearray([
        0x65, 0x11, 0x43, 0x6e, 0xd9, 0xa4, 0x38, 0x6a, 0xc9, 0xb2, 0xc5, 0x2d, 0xaf, 0x9c, 0x64, 0x9f,
        0xc6, 0x61, 0xb6, 0x78, 0xfb, 0x90, 0xd3, 0x97, 0x7c, 0x9c, 0x18, 0x79, 0xc5, 0xa5, 0xd9, 0xeb,
    ])


if __name__ == '__main__':
    test_empty()
    test_abc()
    test_hello_world()
    test_chain()
