#![no_std]
#![no_main]

use core::arch::asm;

fn exit(code: i8) -> ! {
    unsafe {
        asm!("mv a0, {0}",
             "li a7, 93",
             "ecall",
             in(reg) code,
        )
    }
    loop {}
}

#[panic_handler]
fn panic_handler(_: &core::panic::PanicInfo) -> ! {
    exit(-128);
}

#[unsafe(no_mangle)]
fn abort() -> ! {
    panic!("abort!")
}

#[unsafe(no_mangle)]
fn _start() -> ! {
    let mut message = [0u8; 64];
    for _ in 0..25_000 {
        let mut hasher = ckb_opt_fips202::Sha3_512::new();
        hasher.update(&message);
        hasher.finalize(&mut message);
    }
    let want: [u8; 64] = [
        0x27, 0x61, 0x8a, 0x23, 0xdc, 0x03, 0xbd, 0x99, 0xd1, 0xcf, 0x83, 0x8d, 0x1b, 0x45, 0xb4, 0x74, 0x8f, 0xd0,
        0x55, 0xba, 0x7e, 0xeb, 0x4f, 0x59, 0xf2, 0x70, 0x17, 0x70, 0x60, 0xa9, 0xae, 0xd6, 0x38, 0x03, 0x48, 0xf6,
        0x87, 0x65, 0x4f, 0x15, 0xdc, 0x52, 0x62, 0x38, 0x12, 0x2a, 0x44, 0xa8, 0x98, 0xd5, 0xc4, 0x88, 0x59, 0x6b,
        0xed, 0x60, 0x30, 0x10, 0x17, 0x5e, 0x3a, 0xe9, 0x0d, 0xa3,
    ];
    assert_eq!(message, want);
    exit(0)
}
